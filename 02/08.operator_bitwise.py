# ~

print(~5)
print(~-1)

# << 연산자
a = 4
print(a >> 1)
# 0000 0100 (4)
# 0000 0010 (2)
# 뒤로 1칸 미는 것

# and, or, exclusive or(배타적 논리합)
a = 3
print(a & 2)
# 0000 0011 (3)
# 0000 0010 (2)
# &
# 0000 0010 (2)
# 출력값 : 2

print(a | 8)
# 0000 0011 (3)
# 0000 1000 (8)
# |(or)
# 0000 1011 (11)

print(0x0f ^ 0x06)
# 배타적 논리합 : 같으면 0, 다르면 1(배타. True)
# 0000 1111 (0x0f)
# 0000 0110 (0x06)
# 같으면 0, 다르면 1
# 0000 1001 (9)


